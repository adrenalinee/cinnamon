package org.cinnamon.common.mapping.vo;

/**
 * 
 * @author 동성
 * @since 2015. 2. 27.
 */
public class NameValuePair {
	
	private String name;
	
	private String value;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
}
